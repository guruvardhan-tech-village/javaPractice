    private void delbusbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delbusbtnActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();

        if(row == -1){

            JOptionPane.showMessageDialog(this,"Please select a bus");

            return;

        }
        String busno = (String) jTable1.getValueAt(row,0);
        BusAdmin addao = new BusAdmin();
        
        int confirm = JOptionPane.showConfirmDialog(
        this,
        "Confirm delete this Bus?",
        "Confirm",
        JOptionPane.YES_NO_OPTION
        );

        if(confirm != JOptionPane.YES_OPTION){
            return;
        }
        
        try {
            addao.delBus(busno);
        } catch (ClassNotFoundException ex) {
            System.getLogger(BusDetails.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        } catch (SQLException ex) {
            System.getLogger(BusDetails.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }
        

        JOptionPane.showMessageDialog(this,"Bus Deleted Successfully");
        jTable1.clearSelection();
    }//GEN-LAST:event_delbusbtnActionPerformed

