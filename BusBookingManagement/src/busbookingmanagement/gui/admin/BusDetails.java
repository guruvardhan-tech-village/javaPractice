    public void loadBusTable(){

        try{
            BusDAO dao = new BusDAO();
            ResultSet rs = dao.getAllBuses();

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

            model.setRowCount(0); // clear old rows

            while(rs.next()){

                Object row[] = new Object[5];

                row[0] = rs.getInt("BusID");
                row[1] = rs.getString("BusNo");
                row[2] = rs.getString("BusName");
                row[3] = rs.getString("Source");
                row[4] = rs.getString("Destination");

                model.addRow(row);

            }

        }catch(Exception e){
            e.printStackTrace();
        }

    }
